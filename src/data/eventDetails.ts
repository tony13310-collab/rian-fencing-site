// Per-event detail data for Standard Reports

export interface PoolBout {
  opponent: string;
  club: string;
  score: string; // Full score: "5-3", "4-5"
  win: boolean;
}

export interface PoolResult {
  poolNumber: number;
  seed: number | null;
  bouts: PoolBout[];
  wins: number;
  losses: number;
  touchesScored: number;
  touchesReceived: number;
  indicator: number;
  poolRank: number | null;
  poolSize: number | null;
  deSeed: number | null;
}

export interface DEBout {
  round: string;
  opponent: string;
  club: string;
  score: string;
  win: boolean;
}

export interface EventDetail {
  id: string;
  date: string;
  tournament: string;
  event: string;
  location: string;
  place: number;
  total: number;
  rating?: string;
  pool?: PoolResult;
  de?: DEBout[];
  ftlEventId?: string;
}

export function makeEventId(date: string, event: string): string {
  return `${date}_${event.replace(/[^a-zA-Z0-9]/g, "-").toLowerCase()}`;
}

